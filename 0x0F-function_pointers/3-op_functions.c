#include "3-calc.h"

/**
 * op_add - Adds 2 integers
 * @a: Integer 1
 * @b: Integer 2
 *
 * Return: result
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - Subtract b from a
 * @a: Interger 1
 * @b: Integer 2
 *
 * Return: Result
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - Multiply 2 integers
 * @a: Int 1
 * @b: int 2
 *
 * Return: result
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - Divided a by b
 * @a: Int 1
 * @b: Int 2
 *
 * Return: result
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - returns the remainder of a by b
 * @a: Int 1
 * @b: Int 2
 *
 * Return: result
 */
int op_mod(int a, int b)
{
	return (a % b);
}
